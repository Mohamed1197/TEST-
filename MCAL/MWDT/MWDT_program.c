/*******************************************************************************************************/
/*                                     WDT Enable Function                                             */
/*******************************************************************************************************/

void MWDT_voidEnable(void)
{
	/* Select Prescaler => 2.1 Sec */
	WDT_u8_WDTCR_REG =0b00001111;
}
/*#####################################################################################################*/


/*******************************************************************************************************/
/*                                    WDT Disable Function                                             */
/*******************************************************************************************************/
void MWDT_voidDisable(void)
{
	/* Turn OFF Enable */
	WDT_u8_WDTCR_REG |= (1<<4)|(1<<3);
	
	WDT_u8_WDTCR_REG = 0x00;
}
